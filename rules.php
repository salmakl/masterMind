	<center><h1>Mastermind Rules :</h1></center>

	<h2>Goal of the game</h2>

<p>
Compose combinations of numbers difficult to guess and guess as quickly as possible.
</p>
	<h2>Numbers of players</h2>
	<p>
	One player only.</p>
	<h2>Rules of the game</h2>
<p>
Start by determining which player will start composing a combination.</br>
The players therefore compose a combination of numbers.</br>
He presents a first combination proposal to try to find the original.</br>
No number is repeated
</p>
<p>
• If the combination is false or has faults:</br>
- The computer presents the original combination by bursts.<br>
- It displays the 'Wrong place' flag if: a number is in the combination but not in the right place.<br>
- It displays the flag 'Right place' if: a number is in the combination and well placed.<br>
- It displays the flag 'Doses not execit' if: a number is not in the combination.<br>
- Using these clues, the player tries a combination again.<br>
- The players continue thus, limiting themselves to 8 proposals maximum.<br>
</p>
<p>
• If the combination is correct:<br>
- The round is over. The player could play another round.<br>

To win a round, you have to guess the secret combination in up to 8 attempts or compose a combination.<br>
</p>
